function interpolate(template, params) {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_match, key) => {
    const value = params[key];
    return value === undefined || value === null ? "" : String(value);
  });
}

function translate(lang, key, params) {
  const table = DICT[lang] || DICT.en;
  const fallback = DICT.en[key];
  const template = table[key] ?? fallback ?? key;
  return interpolate(template, params);
}

const I18nContext = createContext(null);

export function I18nProvider({ children, defaultLanguage = "en" }) {
  const [language, setLanguage] = useState(() => {
    try {
      const saved = localStorage.getItem(STORAGE_KEY);
      return saved && LANGUAGES[saved] ? saved : defaultLanguage;
    } catch {
      return defaultLanguage;
    }
  });

  useEffect(() => {
    try {
      localStorage.setItem(STORAGE_KEY, language);
    } catch {
      // ignore storage failures
    }
  }, [language]);

  const tr = useCallback((key, params) => translate(language, key, params), [language]);

  const value = useMemo(
    () => ({
      language,
      setLanguage,
      tr,
      languageMeta: LANGUAGES[language] || LANGUAGES.en,
    }),
    [language, tr],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return ctx;
}
